#Operators in Python

#Arithmetic Operators
a = 10
b = 3
print(f"The Sum Of {a} and {b} is { a + b}")  # 13
print(f"The difference of {a} and {b} is {a- b}")  # 7
print(a * b)  # 30
print(a / b)  # 3.3333
print(a // b) # 3
print(a % b)  # 1
print(a ** b) # 1000

#Comparison Operators
x = 5
y = 3
print(x == y)  # False
print(x != y)  # True
print(x > y)   # True
print(x < y)   # False
print(x >= 5)  # True
print(y <= 2)  # False

#Logical Operators
a = True
b = False
print(a and b)  # False
print(a or b)   # True
print(not a)    # False



