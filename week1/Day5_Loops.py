# basic for looop
print("Basic For Loop")
for i in range(5):
    print(i)

print("\n")

# print 1 to 5 numbers
print("1 to 5 numbers")
for i in range(1, 5):
    print(i)

print("\n")
# print string value
print("String Value")
name = "Saurabh"
for ch in name:
    print(ch)

print("\n")
# print list
print("List")
fruits = ["apple", "banana", "mango"]

for fruit in fruits:
    print(fruit)

